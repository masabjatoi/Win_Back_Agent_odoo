        if current_stage == 'email_1_sent':
            stage_num = "2"
        elif current_stage == 'email_2_sent':
            stage_num = "3"

        # Create or update winback.campaign record
        if not campaign:
            self.env['winback.campaign'].create({
                'partner_id': self.id,
                'stage': 'email_1_sent',
                'status': 'active',
                'email_1_sent_date': fields.Datetime.now()
            })
        else:
            if campaign.stage == 'none':
                campaign.write({
                    'stage': 'email_1_sent',
                    'status': 'active',
                    'email_1_sent_date': fields.Datetime.now()
                })
            elif campaign.stage == 'email_1_sent':
                campaign.write({
                    'stage': 'email_2_sent',
                    'status': 'active',
                    'email_2_sent_date': fields.Datetime.now()
                })
            elif campaign.stage == 'email_2_sent':
                campaign.write({
                    'stage': 'email_3_sent',
                    'status': 'active',
                    'email_3_sent_date': fields.Datetime.now()
                })

        # Mark lead as processed
        self.write({
            'lisa_wb_processed': True
        })

