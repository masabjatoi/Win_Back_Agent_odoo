    def message_post(self, **kwargs):
        res = super(ResPartner, self).message_post(**kwargs)
        message_type = kwargs.get('message_type')
        # Only fire for genuine inbound emails from external senders
        if message_type == 'email':
            author_id = kwargs.get('author_id')
            # Skip if author is an internal Odoo user (not a customer)
            if not author_id:
                campaign = self.env['winback.campaign'].search([
                    ('partner_id', '=', self.id),
                    ('stage', 'not in', ['replied', 'reactivated', 'cold'])
                ], limit=1)
                if campaign:
                    campaign.write({'stage': 'replied', 'status': 'completed'})
                    # Create To-Do activity for the salesperson
                    if self.user_id:
                        self.env['mail.activity'].create({
                            'res_model_id': self.env['ir.model']._get('res.partner').id,
                            'res_id': self.id,
                            'activity_type_id': self.env.ref('mail.mail_activity_data_todo').id,
                            'summary': 'Win-Back Campaign - Customer Replied',
                            'note': 'The customer has replied to the win-back campaign email. Please review the customer chatter and follow up.',
                            'user_id': self.user_id.id,
                            'date_deadline': fields.Date.context_today(self)
                        })
        return res

    def action_view_winback_campaign(self):
        self.ensure_one()
        campaign = self.env['winback.campaign'].search([('partner_id', '=', self.id)], limit=1)
        action = self.env["ir.actions.actions"]._for_xml_id("Win_Back_Agent_odoo.action_winback_campaign")
        if campaign:
            action.update({
                'res_id': campaign.id,
                'view_mode': 'form',
                'views': [(False, 'form')],
            })
        else:
            action.update({
                'view_mode': 'form',
                'views': [(False, 'form')],
                'context': {'default_partner_id': self.id},
            })
        return action
