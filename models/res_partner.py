        if not rec_html:
            raise UserError(_("There are no Lisa win-back recommendations or emails drafted for this Customer. Please wait for the agent to process it first."))

        # Find the mail template
        template = self.env['mail.template'].search([
            ('name', '=', 'Lisa Win-Back Campaign Outreach')
        ], limit=1)
        if not template:
            template = self.env.ref('Win_Back_Agent_odoo.lisa_winback_campaign_outreach_template', raise_if_not_found=False)
            
        if not template:
            raise UserError(_("Mail Template 'Lisa Win-Back Campaign Outreach' not found. Please run the setup script or configure it."))

        # Route directly to override email to protect real customers if configured
        company = self.env.company
        recipient_override = company.lisa_wb_recipient_override
        email_values = {}
        if recipient_override:
            email_values.update({
                'email_to': recipient_override,
                'partner_ids': [(6, 0, [])],
                'recipient_ids': [(6, 0, [])]
            })
        
        # Outgoing from salesperson's email as sender
        salesperson = self.user_id
        if salesperson and salesperson.email:
            email_values['email_from'] = salesperson.email_formatted or salesperson.email
        
        # Trigger send
        template.send_mail(self.id, force_send=True, email_values=email_values)

        # Determine the stage number before updating the campaign record
        campaign = self.env['winback.campaign'].search([('partner_id', '=', self.id)], limit=1)
        current_stage = campaign.stage if campaign else 'none'
        
